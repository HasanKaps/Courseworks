/**
 * Simple demonstration of an if condition using Boolean logic.
 * @author Michael Lones, Heriot-Watt University
 */
public class ConditionDemo {

	public static void main(String[] args) {
		int age = 70;
		int wealth = 150000;
		if(age>60 && wealth>100000)
			System.out.println(
			  "Time to retire!");
	}
}
